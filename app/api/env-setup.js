// This file is just for documentation purposes to show what environment variables are needed

// Required environment variables:
// MONGODB_URI - MongoDB connection string (e.g., mongodb+srv://username:password@cluster.mongodb.net/taskmanager)
// NEXTAUTH_SECRET - Secret for NextAuth.js (e.g., a random string like "your-secret-key")
// NEXTAUTH_URL - URL of your application (e.g., http://localhost:3000 in development)
// GOOGLE_CLIENT_ID - Google OAuth client ID (optional, for Google sign-in)
// GOOGLE_CLIENT_SECRET - Google OAuth client secret (optional, for Google sign-in)

// Example .env.local file:
// MONGODB_URI=mongodb+srv://username:password@cluster.mongodb.net/taskmanager
// NEXTAUTH_SECRET=your-secret-key
// NEXTAUTH_URL=http://localhost:3000
// GOOGLE_CLIENT_ID=your-google-client-id
// GOOGLE_CLIENT_SECRET=your-google-client-secret
